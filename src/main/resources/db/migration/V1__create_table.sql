CREATE TABLE public.gasto (
                              id_gasto int4 NOT NULL GENERATED BY DEFAULT AS IDENTITY,
                              monto numeric NOT NULL,
                              fecha_inicio date NOT NULL,
                              fecha_cambio date NOT NULL,
                              uuid_gasto uuid NOT NULL,
                              id_viatico int4 NOT NULL,
                              CONSTRAINT gasto_pk PRIMARY KEY (id_gasto),
                              CONSTRAINT gasto_fk FOREIGN KEY (id_viatico) REFERENCES public.viatico(id_viatico)
);