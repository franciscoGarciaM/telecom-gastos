CREATE TABLE public.viatico (
                                id_viatico int4 NOT NULL GENERATED BY DEFAULT AS IDENTITY,
                                fecha_inicio date NOT NULL,
                                fecha_cambio date NOT NULL,
                                uuid_viatico uuid NOT NULL,
                                monto numeric NOT NULL,
